<div class="b24_section section-text">
<h3>1. What this plugin can do</h3>
<p>This plugin connects your Beds24 account to your Wordpress page to enable commission free online bookings.</p>

<h3>2. REQUIREMENTS - What you need</h3>
<p>To use the plugin you will need a Beds24 account. A free trial is available <a href="http://www.beds24.com/join.html" target="_blank">here</a>.</p>

<h3>3. Individual Hotels and Lodgings</h3>
<p>The plugin can add booking widgets and display an embedded booking page.</p>

<h3>4. Agency Functions</h3>
<p>The plugin can display a search box and live availabilty and  for multiple hotels or lodgings.</p>
<p>You can either embed a Beds24 agency booking page or use posts or XML for search results.</p>

<h3>5. Web Developers</h3>
<p>You can modify the supplied booking widget theme files and copy them into your theme. The plugin will search the theme directory for the filename and use the file located there if it exists.</p>
